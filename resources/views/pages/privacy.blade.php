    <h2>2. Como Usamos suas Informações</h2>
    <p>Utilizamos suas informações para:</p>
    <ul>
        <li>Fornecer, manter e melhorar nossa plataforma;</li>
        <li>Processar transações e enviar avisos relacionados à sua conta;</li>
        <li>Personalizar sua experiência e recomendar conteúdo;</li>
        <li>Monitorar e analisar tendências, uso e atividades relacionadas à nossa plataforma;</li>
        <li>Detectar, prevenir e resolver atividades fraudulentas e outros problemas;</li>
        <li>Cumprir obrigações legais.</li>
    </ul>

